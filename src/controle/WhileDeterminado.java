package controle;

public class WhileDeterminado {

	public static void main(String[] args) {
		
		//Para repetições com numeros determinados usamos a estrutura certa "For"
		
		int  contador = 0;		

		while(contador <=20) {
		System.out.printf("Contador = %d  \n", contador);
		contador +=2;
		}
		
	}
	
}
